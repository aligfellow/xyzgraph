def test_ez_2_butene_rdkit_reference() -> None:
    rdkit = pytest.importorskip("rdkit")
    from rdkit import Chem
    from rdkit.Chem import AllChem

    for smi in [E2_BUTENE, Z2_BUTENE]:
        mol = Chem.MolFromSmiles(smi)
        Chem.AssignStereochemistry(mol, cleanIt=True, force=True)
        expected = None
        for bond in mol.GetBonds():
            if bond.GetBondType() == Chem.BondType.DOUBLE:
                if bond.GetStereo() == Chem.rdchem.BondStereo.STEREOE:
                    expected = "E"
                elif bond.GetStereo() == Chem.rdchem.BondStereo.STEREOZ:
                    expected = "Z"
        assert expected in {"E", "Z"}

        mol = Chem.AddHs(mol)
        AllChem.EmbedMolecule(mol, AllChem.ETKDG())
        AllChem.UFFOptimizeMolecule(mol, maxIters=200)
        conf = mol.GetConformer()
        atoms = []
        for atom in mol.GetAtoms():
            pos = conf.GetAtomPosition(atom.GetIdx())
            atoms.append((atom.GetSymbol(), (float(pos.x), float(pos.y), float(pos.z))))

        G = build_graph(atoms)
        ez = assign_ez(G)
        assert _label_from_map(ez) == expected

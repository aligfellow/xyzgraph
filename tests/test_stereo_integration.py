def _write_xyz(tmp_path: Path, name: str, xyz_text: str) -> Path:
    path = tmp_path / name
    path.write_text(xyz_text)
    return path


def _xyz_from_coords(coords: str, title: str) -> str:
    lines: list[str] = []
    for line in coords.strip().splitlines():
        stripped = line.strip()
        if not stripped:
            continue
        parts = stripped.split()
        if len(parts) == 2 and all(part.lstrip("-").isdigit() for part in parts):
            continue
        if parts[0][0].isalpha():
            lines.append(" ".join(parts))
    return f"{len(lines)}\n{title}\n" + "\n".join(lines) + "\n"


def _label_from_map(labels: dict[tuple[int, int], str]) -> str:
    assert len(labels) == 1
    return next(iter(labels.values()))


